package exercise0706;

public class Exercise0706 {

    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++) {
            for (int j = 1; j <= 9; j++) {
                System.out.print(i * j);
                System.out.print(", ");
            }
            System.out.println();
        }
    }
    
}
